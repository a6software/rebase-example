<?php

/**
 * Class ExampleClass
 */
class ExampleClass
{
    /**
     * @var
     */
    private $property;
    /**
     * @var
     */
    private $secondProperty;


    /**
     * @param $property
     * @param $secondProperty
     */
    public function __construct($property, $secondProperty)
    {
        $this->property;
        $this->secondProperty;
    }

    /**
     * @return mixed
     */
    public function getProperty()
    {
        return $this->property;
    }

    /**
     * @return mixed
     */
    public function getSecondProperty()
    {
        return $this->secondProperty;
    }


}